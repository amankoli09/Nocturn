/**
 * Nocturn API ã main entry point.
 *
 * Exposes `deployNegotiation` and `joinNegotiation` which return a
 * `DeployedNocturnAPI` instance. The API wraps the Compact-generated
 * contract bindings from `managed/negotiation/contract/index.js`.
 *
 * This module is consumed by:
 *   - The React UI (ui/) via WalletContext / NegotiationContext
 *   - The deploy script (scripts/deploy.ts)
 */

import {
  deployContract,
  findDeployedContract,
  type MidnightProviders,
} from '@midnight-ntwrk/midnight-js-contracts';
import type { ContractAddress } from '@midnight-ntwrk/midnight-js-protocol/compact-runtime';
import { combineLatest, map, type Observable } from 'rxjs';
import { createWitnesses, createStubWitnesses, type NegotiationWitnesses } from './witnesses.js';
import {
  decodeStatus,
  type DeployedNocturnAPI,
  type NegotiationState,
} from './types.js';

// ---------------------------------------------------------------------------
// Re-export types so UI only needs to import from @nocturn/api
// ---------------------------------------------------------------------------
export { PREPROD_CONFIG } from './types.js';
export type { DeployedNocturnAPI, NegotiationState, NocturnConfig } from './types.js';
export type { NegotiationWitnesses } from './witnesses.js';
export { createWitnesses, createStubWitnesses } from './witnesses.js';
export { decodeStatus } from './types.js';

// ---------------------------------------------------------------------------
// Compact contract import ã resolved after `npm run compile`
// The managed/ directory is generated by `compactc` from negotiation.compact.
// ---------------------------------------------------------------------------

// We use a dynamic import pattern with type assertion because the managed/
// directory may or may not be present depending on compile state.
// In production the contract IS compiled; in CI tests we skip ZK.
async function loadContract() {
  try {
    // This path resolves relative to the workspace root at runtime
    const mod = await import('../../../managed/negotiation/contract/index.js' as string);
    return mod;
  } catch {
    // Fallback: return a mock for environments without compiled artifacts
    console.warn(
      '[nocturn/api] managed/negotiation not found ã using mock contract.\n' +
        'Run `bash scripts/compile.sh` to generate real artifacts.',
    );
    return null;
  }
}

// ---------------------------------------------------------------------------
// Internal: build the observable state from the contract's public ledger state
// ---------------------------------------------------------------------------

function buildState$(
  contractAddress: ContractAddress,
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  contractInstance: any,
): Observable<NegotiationState> {
  return combineLatest([
    contractInstance.negotiationStatus$,
    contractInstance.agreedPrice$,
  ]).pipe(
    map(([status, price]: [bigint | number, bigint | number]) => ({
      status: decodeStatus(status),
      agreedPrice: BigInt(price),
      contractAddress,
    })),
  );
}

// ---------------------------------------------------------------------------
// Internal: wrap a deployed contract instance into the DeployedNocturnAPI
// ---------------------------------------------------------------------------

function wrapContract(
  contractAddress: ContractAddress,
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  contractInstance: any,
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  providers: MidnightProviders<any, any>,
): DeployedNocturnAPI {
  const state$ = buildState$(contractAddress, contractInstance);

  return {
    contractAddress,
    state$,

    async openNegotiation() {
      const witnesses = createStubWitnesses();
      const tx = await contractInstance.callTx.openNegotiation(witnesses);
      await providers.walletProvider.submitTx(tx);
    },

    async matchOffer(buyerMax: bigint, sellerMin: bigint) {
      // Witnesses are created here ã they exist only for this call's lifetime.
      // buyerMax and sellerMin NEVER appear in the submitted transaction body.
      const witnesses = createWitnesses(buyerMax, sellerMin);
      const tx = await contractInstance.callTx.matchOffer(witnesses);
      await providers.walletProvider.submitTx(tx);
    },
  };
}

